// Operadores Arimeticos ---> +,-,*,/

let num1 = 6;
let num2 = 2;

let puntuacion = num1 - num2;
console.log(`el resultado de la resta ${puntuacion}`)

puntuacion = num1 + num2;
console.log(`el resultado de la suma es ${puntuacion}`)

puntuacion = num1 * num2;
console.log(`el resultado de la multiplicacion es ${puntuacion}`)

puntuacion = num1 / num2;
console.log(`el resultado de la dividion es ${puntuacion}`)

puntuacion = num1 % num2;
console.log(`el modulo de la dividion es ${puntuacion}`)

// Imcremento ++
let c = 1;
console.log(c++); // Postincremento
console.log(c); // le suma una unidad despues

let d = 2;
console.log(++d);  // Preincremento
console.log(d)

